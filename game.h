#ifndef _GAME_H_	//このマクロ定義がされてなかったら
#define _GAME_H_	//２重インクルード防止のマクロ定義

//==================================================
// 列挙型
//==================================================
enum EGameState
{
	GAMESTATE_NONE = 0,	// 何もしていない状態
	GAMESTATE_NORMAL,	// 通常状態
	GAMESTATE_RESULT,	// リザルト状態
	GAMESTATE_MAX
};

//==================================================
// プロトタイプ宣言
//==================================================
//--------------------------------------------------
// 初期化
//--------------------------------------------------
void InitGame(void);

//--------------------------------------------------
// 終了
//--------------------------------------------------
void UninitGame(void);

//--------------------------------------------------
// 更新
//--------------------------------------------------
void UpdateGame(void);

//--------------------------------------------------
// 描画
//--------------------------------------------------
void DrawGame(void);

//--------------------------------------------------
// 設定
// 引数  : EGameState inState / 列挙型 状態
//--------------------------------------------------
void SetGameState(EGameState inState);

//--------------------------------------------------
// 取得
// 返値  : EGameState / 列挙型 状態
//--------------------------------------------------
EGameState GetGameState(void);

//--------------------------------------------------
// ポーズの有効無効設定
// 引数  : bool inPause / ポーズするかどうか [ true : する false : しない ]
//--------------------------------------------------
void SetEnablePause(bool inPause);

//--------------------------------------------------
// ポーズの有効無効取得
// 返値  : bool / ポーズしてるかどうか
//--------------------------------------------------
bool GetEnablePause(void);

#endif // !_GAME_H_
